const mongoose = require("mongoose");
const subTodoSchema = mongoose.Schema(
  {
    content: {
      type: String,
      required: true,
    },
    complete: {
      type: Boolean,
      default: false,
    },
    createdBy: {
      type: mongoose.Schema.Types.ObjectId,
      ref: "User",
    },
  },
  { timestamps: true }
);

const SubTodo = mongoose.model("Subtodo", subTodoSchema);
